template<typename Move>
void MetaBase<Move>::UpdateBest(Solution& sol, const int obj){
    NewBestSolutionFound = false;
    if (obj < best_obj){
        best_obj = obj;
        UpdateBestSolution(sol);
        it_idle_best = 0;
        NewBestSolutionFound = true;
    }
    else{
        ++it_idle_best;
    }
}

