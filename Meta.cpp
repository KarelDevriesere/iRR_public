template<typename Move>
bool SA<Move>::Update(Solution& sol, const int obj){
            // cout << "prev_obj = " << current_obj << ", new obj = " << obj << endl;

            if (this->getTimeDiff() > this->TIME_LIMIT || T < T_end){
                this->STOP = true;
                if (this->getTimeDiff() > this->TIME_LIMIT){
                    cout << "Time limit hit" << endl;
                }
                else{
                    cout << "Final temperature hit after " << this->getTimeDiff() << " seconds" << endl;
                }
                // cin.get();
            }

            this->UpdateValues(sol, obj);
            bool SolutionAccepted = false;
            if (obj <= this->current_obj){
                this->current_obj = obj; 
                this->it_accepted++;
                SolutionAccepted = true;
            }
            else{
                this->dist_real = std::uniform_real_distribution<>(0.0, 1.0);
                double rnd = this->dist_real(this->gen);
                if (rnd <= exp(-(obj-this->current_obj)/T) && obj < INT_MAX-10){ // Metropolis acceptance
                    // cout << "Accept solution with prob " << exp(-(obj-current_obj)/T) << ", prev_obj = " << current_obj << ", new obj = " << obj << endl;
                    this->current_obj = obj;
                    this->it_accepted++;
                    SolutionAccepted = true;
                }
                else{
                    // reverse move
                    // We only need to reverse the haps since the matchings are thrown away anyway
                    // cout << "Reject solution with prob " << exp(-(obj-current_obj)/T) << endl;
                    SolutionAccepted = false; // CUSTOMIZE WHAT HAPPENS AFTERWARDS
                }
            }
            if (SolutionAccepted){
#ifdef PRINT
#if PRINT == 1
                    this->print_solution();
#endif
#endif
            }

            this->UpdateTimeStamps();

            if (++this->it % I_temp == 0 || (this->it_accepted - I_accept >= 0)){ 
                T *= cooling_rate;
#ifdef PRINT
#if PRINT == 1
                cout << "New T = " << T << endl;
#endif
#endif
                this->it_accepted = 0;
                this->it = 0;
            }
            return SolutionAccepted;
}


template<typename Move>
bool HC<Move>::Update(Solution& sol, const int obj){
            
            // cout << "prev_obj = " << this->current_obj << ", new obj = " << obj << endl;

            if (obj >= this->current_obj){
                this->it_idle++;
                if (this->it_idle % 10000 == 0 && this->it_idle > 0){
                    cout << "it_idle = " << this->it_idle << endl;
                }
            }
            else{
                this->it_idle = 0;
                #ifdef PRINT
#if PRINT == 1
                    this->print_solution();
#endif
#endif
            }

            if (this->getTimeDiff() > this->TIME_LIMIT){
                cout << "Time limit of " << this->TIME_LIMIT << " hit" << endl;
                this->STOP = true;
            }
            else if (this->it_idle > MAX_IT){
                cout << "it_idle = " << this->it_idle << ", MAX_IT = " << MAX_IT << endl;
                this->STOP = true;
            }

            this->UpdateValues(sol, obj);
            bool SolutionAccepted = false;
            if (obj <= this->current_obj){
                this->current_obj = obj; 
                this->it_accepted++;
                SolutionAccepted = true;
            }

            this->UpdateTimeStamps();

            return SolutionAccepted;
}

template<typename Move>
bool ILS<Move>::Update(Solution& sol, const int obj){

            bool SolutionAccepted = false;

            if (this->current_obj < obj && !this->PERTURBE){
                ++this->it_idle;
            }
            if (this->current_obj >= obj || (this->PERTURBE && obj < 3*sol.getCostTTPViolation()-100)){
                this->current_obj = obj;
                SolutionAccepted = true;

                if (this->PERTURBE){
                    it_accepted_perturbation++;
                }
#if PRINT == 1
                    this->print_solution();
#endif
            }

            if (this->getTimeDiff() > this->TIME_LIMIT){
                cout << "Time limit of " << this->TIME_LIMIT << " hit" << endl;
                this->STOP = true;
            }

            this->UpdateTimeStamps();

            return SolutionAccepted;
}


